#include <unistd.h>
void	print(char nb)
{
	write(1, &nb, 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= 99)
	{
		b = a + 1;
		while (b <= 99)
		{
			print('0' + a / 10);
			print('0' + a % 10);
			print(' ');
			print('0' + b / 10);
			print('0' + b % 10);
			print(',');
			b++;
		}
		a++;
	}
}
