#include <unistd.h>
void	ft_print_comb(void)
{
	int	a;
	int	b;
	int	c;

	a = 0x30;
	while (a <= 0x37)
	{
		b = a + 1;
		while (b <= 0x38)
		{
			c = b + 1;
			while (c <= 0x39)
			{
				write(1, &a, 1);
				write(1, &b, 1);
				write(1, &c, 1);
				if (!(a == '7' && b == '8' && c == '9'))
					write(1, ", ", 2);
				c++;
			}
			b++;
		}
		a++;
	}
}
